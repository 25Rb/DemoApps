package be.infrbru.model;

public class Employer {

	private String id;
	private String wgrNr;
	private String name;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getWgrNr() {
		return wgrNr;
	}

	public void setWgrNr(String wgrNr) {
		this.wgrNr = wgrNr;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
